import { readFile, writeFile } from "node:fs/promises";

const assets = [
  { route: "/favicon.ico", path: "assets/images/favicon/favicon.ico", contentType: "image/x-icon" },
  { route: "/assets/images/favicon/favicon-32x32.png", path: "assets/images/favicon/favicon-32x32.png", contentType: "image/png" },
  { route: "/assets/images/favicon/apple-icon-180x180.png", path: "assets/images/favicon/apple-icon-180x180.png", contentType: "image/png" },
  { route: "/assets/images/cocktails.svg", path: "assets/images/cocktails.svg", contentType: "image/svg+xml" },
  { route: "/assets/images/appstore.png", path: "assets/images/appstore.png", contentType: "image/png" },
  { route: "/assets/images/playmarket.png", path: "assets/images/playmarket.png", contentType: "image/png" },
];

let output = `// This file is generated by tools/generate-static-assets.mjs.\n// Do not edit directly; update assets/images/* and rerun npm run generate-static-assets.\n\ntype StaticAsset = {\n  contentType: string;\n  bodyBase64: string;\n};\n\nconst STATIC_ASSETS: Record<string, StaticAsset> = {\n`;

for (const asset of assets) {
  const bodyBase64 = (await readFile(asset.path)).toString("base64");
  output += `  ${JSON.stringify(asset.route)}: {\n    contentType: ${JSON.stringify(asset.contentType)},\n    bodyBase64: ${JSON.stringify(bodyBase64)},\n  },\n`;
}

output += `};\n\nfunction base64ToArrayBuffer(value: string): ArrayBuffer {\n  const binary = atob(value);\n  const bytes = new Uint8Array(binary.length);\n  for (let index = 0; index < binary.length; index += 1) {\n    bytes[index] = binary.charCodeAt(index);\n  }\n  return bytes.buffer;\n}\n\nexport function staticAssetResponse(path: string): Response | null {\n  const asset = STATIC_ASSETS[path];\n  if (!asset) return null;\n\n  return new Response(base64ToArrayBuffer(asset.bodyBase64), {\n    headers: {\n      \"Content-Type\": asset.contentType,\n      \"Cache-Control\": \"public, max-age=31536000, immutable\",\n      \"X-Content-Type-Options\": \"nosniff\",\n    },\n  });\n}\n`;

await writeFile("src/static-assets.ts", output);
